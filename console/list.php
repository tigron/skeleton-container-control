namespace Skeleton\Console\Command;

use Symfony\Component\Console\Input\InputArgument;
use Symfony\Component\Console\Input\InputInterface;
use Symfony\Component\Console\Input\InputOption;
use Symfony\Component\Console\Output\OutputInterface;
use Symfony\Component\Console\Helper\Table;
use Symfony\Component\Console\Helper\TableCell;
use Symfony\Component\Console\Helper\TableSeparator;
use Symfony\Component\Console\Helper\TableStyle;
use Skeleton\Container\Container;

class Container_List extends \Skeleton\Console\Command {

	/**
	 * Configure the Create command
	 *
	 * @access protected
	 */
	protected function configure() {
		$this->setName('container:list');
		$this->setDescription('List all paired containers');
	}

	/**
	 * Execute the Command
	 *
	 * @access protected
	 * @param InputInterface $input
	 * @param OutputInterface $output
	 */
	protected function execute(InputInterface $input, OutputInterface $output) {
		$containers = Container::get_all();

		$table = new Table($output);

		$table->setHeaders(['ID', 'Name', 'Endpoint']);

		$rows = [];
		foreach ($containers as $container) {
			$rows[] = [ $container->id, $container->name, $container->endpoint ];
		}

		$table->setRows($rows);
		$table->render();

		return 0;
	}

}
